package controleDeFluxo

fun maiorDeIdade(idade: Int) {
    if (idade >= 18) {
        println("Maior de idade!")
    } else {
        println("Não é maior de idade")
    }
}

// Obs.: Para identar o códio no mac é command + alt 'L'

fun main() {
    maiorDeIdade(10)
}

